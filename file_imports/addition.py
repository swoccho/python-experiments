class Addition:
    @classmethod
    def add(cls,num1,num2):
        return num1 + num2


    @classmethod
    def multiply(cls,num1,num2):
        return num1 * num2

    @classmethod
    def subtract(cls,num1,num2):
        return num1 - num2

    @classmethod
    def divide(cls, num1 , num2):
        return num1 / num2
